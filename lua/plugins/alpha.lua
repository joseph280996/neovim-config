return {
  "goolord/alpha-nvim",
  dependencies = {
    "kyazdani42/nvim-web-devicons",
  },
  event = "VimEnter",
  config = function()
    local status_ok, alpha = pcall(require, "alpha")
    if not status_ok then
      return
    end

    -- Settings
    local dashboard = require("alpha.themes.dashboard")

    local function button(sc, txt, keybind, keybind_opts)
      local b = dashboard.button(sc, txt, keybind, keybind_opts)
      return b
    end

    local icons = require("utils.constants.icons")

    dashboard.section.header.val = headers.proud_cat
    dashboard.section.buttons.val = {
      button("p", icons.git.Repo .. "  Find project", ":lua Snacks.picker.projects() <CR>"),
      button("r", icons.ui.History .. "  Recent files", ":lua Snacks.picker.recent() <CR>"),
      button("f", icons.documents.Files .. "  Find file", ":lua Snacks.picker.files() <CR>"),
      button("t", icons.ui.List .. "  Find text", ":lua Snacks.picker.grep() <CR>"),
      button("e", icons.ui.NewFile .. "  New file", ":ene <BAR> startinsert <CR>"),
      button("P", icons.ui.Package .. "  Packages", "<cmd>Lazy<cr>"),
      button("q", icons.ui.SignOut .. "  Quit", ":qa<CR>"),
    }
    local function footer()
      -- NOTE: requires the fortune-mod package to work
      -- local handle = io.popen("fortune")
      -- local fortune = handle:read("*a")
      -- handle:close()
      -- return fortune
      local date = os.date("  %d/%m/%Y ")
      local time = os.date("  %H:%M:%S ")

      local v = vim.version()
      local version = "  v" .. v.major .. "." .. v.minor .. "." .. v.patch

      return date .. time .. version
    end

    dashboard.section.footer.val = footer()
    vim.api.nvim_set_hl(0, "AlphaHeader", { fg = "#98BB6C" })
    vim.api.nvim_set_hl(0, "AlphaFooter", { fg = "#7AA89F" })

    dashboard.section.header.opts.hl = "AlphaHeader"
    dashboard.section.buttons.opts = { spacing = 1, hl = "Type" }
    dashboard.section.footer.opts.hl = "AlphaFooter"

    alpha.setup(dashboard.opts)
  end,
  keys = {
    {
      "<leader>;",
      "<cmd>Alpha<cr>",
      desc = "Alpha",
      mode = "n",
      silent = true,
      noremap = true,
      nowait = true,
    },
  },
